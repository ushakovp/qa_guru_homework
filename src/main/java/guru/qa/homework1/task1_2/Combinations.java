package guru.qa.homework1.task1_2;

public class Combinations {
    public static void main(String[] args) {
        byte oByte = 1;
        short oShort = 1;
        int oInt = 1;
        long oLong = 1;
        float oFloat = 1;
        double oDouble = 1;
        char oChar = 1;

        System.out.println(oByte + oShort);
        System.out.println(oInt + oLong);
        System.out.println(oFloat + oDouble);
        System.out.println(oInt + oDouble);
        System.out.println(oFloat + oLong);
        System.out.println(oChar + oInt);
        System.out.println(oFloat + oDouble);

    }
}
